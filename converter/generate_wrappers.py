"""Generate per-persona wrapper skills (Tier 3).

Each entry in `dist/references/agent-prompts/manifest.json` becomes a thin
SKILL.md at `dist/skills/ce-ask-<persona>/SKILL.md`. The wrapper restores
addressability through Claude's skill-routing surface — Claude can
spontaneously suggest `/ce-ask-security-sentinel` when reviewing
security-sensitive code, without re-paying the persistent-agent
registration cost (49 agents × ~1.2k tokens = ~58.8k baseline that the
ce-lite v1 converter strips).

Naming convention: `ce-ask-<persona-bare-name>`. The `ce-` prefix is
stripped from the agent name before re-prefixing with `ce-ask-` so we
get `ce-ask-security-sentinel` rather than `ce-ask-ce-security-sentinel`.
The canonical persona name (`ce-security-sentinel`) stays referenced in
the wrapper body — that's what gets dispatched to the persona prompt
file. Naming this way keeps the canonical names available for a future
Tier 4 (lazy agent registration) re-registration without a name fight.

Description generation is two-pass:

  - Pass A (this script, deterministic): strip the "Conditional X persona,
    selected when..." agent-routing preamble where present; otherwise pass
    the manifest description through unchanged. Cheap baseline.
  - Pass B (run separately, locally, before each release): drive
    `document-skills:skill-creator`'s `improve_description.py` over the
    full set; commit refined descriptions to overrides/persona-descriptions.yaml;
    re-run this generator with overrides applied. Not a CI step (no LLM
    in CI invariant from the parent converter plan).

If `overrides/persona-descriptions.yaml` exists in the converter repo
(sibling to this script), per-persona overrides take precedence over
Pass A output.

The wrapper template embeds:

  1. A "do not edit" marker so round-trip cleanup can identify generated
     files and Tier 4 migration knows what to delete.
  2. A `[ce-persona=<n> via=ce-ask-direct]` trace tag for inline
     debugging (grep transcripts when a routing question comes up; not
     systematic usage data collection).
  3. A tool-restriction self-policing preamble that turns the manifest's
     advisory `tools:` field into loud-advisory inside the dispatched
     prompt (the harness can't enforce because we dispatch via
     general-purpose, but the agent can self-police if explicitly told).
  4. A missing-prompt-file fallback that reports the rename rather than
     silently failing.
  5. The canonical persona name in the body (never the wrapper name) so
     Tier 4 migration is a mechanical s/// over wrapper-bodies.
"""

from __future__ import annotations

import argparse
import json
import re
import sys
from dataclasses import dataclass
from pathlib import Path

CONDITIONAL_PREFIX_RE = re.compile(
    r"^Conditional\s+[a-z][a-z\-/]*\s+persona,\s+selected\s+when\s+",
    re.IGNORECASE,
)

WRAPPER_GENERATED_MARKER = (
    "<!-- Generated by ce-lite converter — do not edit. "
    "Regenerate via converter/generate_wrappers.py. -->"
)


# Persona prompts live inside this plugin's install dir, but the install path
# varies per machine and a bare relative path resolves relative to the user's
# project (which is wrong). Every dispatch site needs the same discovery
# protocol — keep it as one canonical block so the four templates stay in sync.
PROMPT_DISCOVERY_INSTRUCTIONS = """\
> **Locating the plugin's persona prompts.** A bare `references/agent-prompts/`
> path resolves relative to the user's project, which is usually not where
> this plugin is installed. Use Glob (rooted at `~`) to find the actual path:
>
> ```
> **/.claude/plugins/cache/ce-lite/*/references/agent-prompts/<name>.md
> ```
>
> If that pattern returns nothing, fall back to `Bash`:
>
> ```
> find ~/.claude/plugins/cache -name '<name>.md' -path '*/agent-prompts/*' 2>/dev/null | head -1
> ```
>
> Cache the discovered plugin root (the directory containing
> `.claude-plugin/plugin.json`) — every persona prompt lives under the same
> root, so subsequent lookups in this turn don't need to re-glob."""


@dataclass(frozen=True)
class Persona:
    name: str
    description: str
    model: str
    tools: list[str] | None
    prompt_path: str


def load_manifest(dist: Path) -> list[Persona]:
    manifest_path = dist / "references" / "agent-prompts" / "manifest.json"
    if not manifest_path.is_file():
        raise FileNotFoundError(f"missing manifest: {manifest_path}")
    raw = json.loads(manifest_path.read_text(encoding="utf-8"))
    return [
        Persona(
            name=a["name"],
            description=a["description"],
            model=a.get("model") or "inherit",
            tools=a.get("tools"),
            prompt_path=a["prompt_path"],
        )
        for a in raw["agents"]
    ]


def load_overrides(converter_dir: Path) -> dict[str, str]:
    """Load overrides/persona-descriptions.yaml as a flat name→description map.

    Format (light-touch YAML, no nesting):

        ce-security-sentinel: |
          Use when reviewing for security issues...
        ce-architecture-strategist: "Use when ..."

    Empty file or missing file returns {}. Keep the parser deliberately
    minimal — overrides are populated by skill-creator and committed to
    the repo, not edited at scale by hand.
    """
    overrides_path = converter_dir / "overrides" / "persona-descriptions.yaml"
    if not overrides_path.is_file():
        return {}
    text = overrides_path.read_text(encoding="utf-8")
    result: dict[str, str] = {}
    current_key: str | None = None
    current_val: list[str] = []
    block_indent: int | None = None
    for raw_line in text.splitlines():
        # Skip pure comments / blank lines at top level.
        stripped = raw_line.strip()
        if not stripped or stripped.startswith("#"):
            if current_key is None:
                continue
        # Top-level key (no leading whitespace, has colon).
        if raw_line and not raw_line[0].isspace() and ":" in raw_line:
            # Flush previous key.
            if current_key is not None:
                result[current_key] = "\n".join(current_val).rstrip()
                current_val = []
                block_indent = None
            key, _, value = raw_line.partition(":")
            current_key = key.strip()
            value = value.strip()
            if value in ("|", ">", "|-", ">-"):
                # Block scalar follows on indented lines.
                continue
            if value:
                # Inline scalar; strip optional quotes.
                if (value.startswith('"') and value.endswith('"')) or (
                    value.startswith("'") and value.endswith("'")
                ):
                    value = value[1:-1]
                result[current_key] = value
                current_key = None
                current_val = []
                block_indent = None
            continue
        # Indented continuation of a block scalar.
        if current_key is not None and raw_line and raw_line[0].isspace():
            # Establish block indent on first content line.
            indent = len(raw_line) - len(raw_line.lstrip())
            if block_indent is None:
                block_indent = indent
            # Strip the common indent.
            current_val.append(raw_line[block_indent:])
    if current_key is not None:
        result[current_key] = "\n".join(current_val).rstrip()
    return result


def wrapper_name(persona_name: str) -> str:
    """ce-security-sentinel → ce-ask-security-sentinel."""
    if persona_name.startswith("ce-"):
        return f"ce-ask-{persona_name[3:]}"
    return f"ce-ask-{persona_name}"


def passA_description(manifest_desc: str) -> str:
    """Strip the agent-routing 'Conditional X, selected when...' preamble.

    Most upstream descriptions are already action-oriented; only the
    'Conditional' family of personas reads as agent-routing language and
    benefits from rewriting. Pass through unchanged otherwise — Pass B
    (skill-creator-driven) will tune the rest.
    """
    if CONDITIONAL_PREFIX_RE.match(manifest_desc):
        rest = CONDITIONAL_PREFIX_RE.sub("", manifest_desc, count=1)
        return f"Use when {rest}"
    return manifest_desc


def render_wrapper(persona: Persona, description: str) -> str:
    tools_str = ", ".join(persona.tools) if persona.tools else "any"
    return f"""\
---
name: {wrapper_name(persona.name)}
description: {json.dumps(description)}
---

{WRAPPER_GENERATED_MARKER}

Dispatch the **{persona.name}** specialist by spawning a sub-agent with the
persona's prompt body as preamble. The persona is one of the lightweight-
delegation specialists in this plugin; its prompt body lives at
`{persona.prompt_path}` **inside the plugin's install directory** (NOT the
user's project — see discovery instructions below).

{PROMPT_DISCOVERY_INSTRUCTIONS}

## Steps

1. Locate the prompt file via the discovery instructions above. Then read
   the resolved path (`<plugin-root>/{persona.prompt_path}`) for the
   persona's full role definition.
2. Spawn a new agent with `subagent_type: "general-purpose"` (the harness's
   default fan-out type — persistent registrations were stripped to keep this
   plugin's idle context cost low). Prepend the following preamble to the
   prompt before the user-supplied task context:

   ```
   [ce-persona={persona.name} via=ce-ask-direct]

   You are operating as the {persona.name} specialist. Your role is defined
   in the prompt body above. Manifest declares this role uses
   tools=[{tools_str}] and model={persona.model}. Honour those constraints:
   if a task pulls you toward tools outside that set, stop and explain why
   your role requires it — don't silently broaden scope.
   ```

3. Pass the user-supplied task context after the preamble.

## Missing-prompt fallback

If the discovery step returns no plugin root, OR the resolved
`<plugin-root>/{persona.prompt_path}` does not exist (upstream rename,
partial install), report:

> Persona `{persona.name}` not found in this plugin's manifest. The plugin
> may not be installed, may have been partially installed, or the persona
> may have been renamed upstream. Re-install the plugin or regenerate the
> converter dist.

Do **not** silently fall back to a different persona — the user explicitly
named this specialist.

## Notes

- Trace tag `[ce-persona={persona.name} via=ce-ask-direct]` is inline debug
  metadata. `grep` transcripts when investigating routing or behaviour
  questions; no systematic collection.
- For a multi-persona panel, see `/ce-ask-panel`.
"""


def write_wrapper(dist: Path, persona: Persona, description: str) -> Path:
    skill_dir = dist / "skills" / wrapper_name(persona.name)
    skill_dir.mkdir(parents=True, exist_ok=True)
    skill_path = skill_dir / "SKILL.md"
    skill_path.write_text(render_wrapper(persona, description), encoding="utf-8")
    return skill_path


PANEL_DESCRIPTION = (
    "Use to consult several persona specialists in parallel and merge their "
    "findings into one response. Provide a comma-separated list of persona "
    "names plus the task context. Bridges single-persona reach (one ce-ask-* "
    "skill) and full orchestrator pipelines (ce-code-review etc.) when you "
    "want N specific perspectives but not the full review pipeline."
)


def render_panel(personas: list[Persona]) -> str:
    """Render the ce-ask-panel meta-skill body.

    Static template — same regardless of which personas exist; the runtime
    validates against the live manifest. Listing personas at generation time
    would force regeneration on every upstream tag bump just to update a list
    Claude can read from manifest.json directly.
    """
    return f"""\
---
name: ce-ask-panel
description: {json.dumps(PANEL_DESCRIPTION)}
argument-hint: "<persona1>,<persona2>,... <task context>"
---

{WRAPPER_GENERATED_MARKER}

Dispatch a panel of multiple persona specialists in parallel and merge their
findings into one response. Bridges single-persona ad-hoc reach
(`/ce-ask-<one-persona>`) and the full orchestrator pipelines
(`/ce-code-review`, `/ce-doc-review`, `/ce-resolve-pr-feedback`) — use this
when you want N specific perspectives without the rest of the pipeline.

## Usage

`/ce-ask-panel <persona1>,<persona2>,... <task context>`

Persona names are the **canonical** names from the plugin's persona manifest
(e.g. `ce-security-sentinel`, `ce-architecture-strategist`), NOT the
`ce-ask-*` wrapper names.

{PROMPT_DISCOVERY_INSTRUCTIONS}

## Steps

1. Parse the first whitespace-delimited token as a comma-separated persona
   list. Trim each name. Treat the rest of the input as the user's task
   context.
2. Locate the plugin root via the discovery instructions above and read
   `<plugin-root>/references/agent-prompts/manifest.json`. Validate each
   named persona is in `agents[].name`. If any are unknown:

   > Unknown persona(s): `<list>`. Known personas: `<list-from-manifest>`.
   > Use the canonical names (e.g. `ce-security-sentinel`), not wrapper
   > names (`ce-ask-security-sentinel`).

   Stop without dispatching anything — partial dispatches confuse the
   merged output.
3. For each validated persona, dispatch in parallel:
   - Read its prompt body from
     `<plugin-root>/references/agent-prompts/<persona>.md`.
   - Spawn an agent with `subagent_type: "general-purpose"`.
   - Prepend a preamble of the form:

     ```
     [ce-persona=<persona> via=ce-ask-panel]

     You are operating as the <persona> specialist. Your role is defined in
     the prompt body above. Honour the manifest's tools/model constraints
     for this role; if a task pulls you toward tools outside that set, stop
     and explain why your role requires it.
     ```

   - Then the user-supplied task context.
4. Wait for all to complete. Merge into a single response, grouping by
   persona with section headers (`## <persona> findings`). Preserve each
   persona's structured output rather than collapsing into prose.

## Notes

- Trace tag `via=ce-ask-panel` distinguishes panel dispatch from direct
  invocation (`via=ce-ask-direct`) when grepping transcripts.
- For one persona, prefer `/ce-ask-<persona-bare-name>` — slightly cheaper
  than going through the panel parser.
- For all-personas-pertinent reviews of code or documents, prefer the
  orchestrator skills (`/ce-code-review`, `/ce-doc-review`) which include
  conditional persona selection logic.
"""


def write_panel(dist: Path, personas: list[Persona]) -> Path:
    skill_dir = dist / "skills" / "ce-ask-panel"
    skill_dir.mkdir(parents=True, exist_ok=True)
    skill_path = skill_dir / "SKILL.md"
    skill_path.write_text(render_panel(personas), encoding="utf-8")
    return skill_path


META_SKILL_DESCRIPTION = (
    "Discover and dispatch ce-lite specialist personas via a single slash command. "
    "Run with no args to browse the persona catalog (security, architecture, "
    "correctness, simplicity, pattern, performance, data-integrity, doc review, "
    "and more). Run with `<persona-name>` to see one persona's role. Run with "
    "`<persona-name> <task>` to dispatch that specialist on the task. For "
    "tab-complete-friendly invocation use `/ce-ask-<persona>` directly; for "
    "parallel multi-persona dispatch use `/ce-ask-panel`."
)


META_AGENT_DESCRIPTION = (
    "Code-review/architecture/security/correctness/simplicity/pattern specialist. "
    "Delegate to this agent when reviewing code or documents that benefit from a "
    "focused specialist's eyes — even tasks the main agent could handle, the "
    "specialist provides an independent perspective uncoloured by current "
    "conversation context. Routes internally to one of ~49 persona prompts "
    "(catalogued in references/agent-prompts/manifest.json). Pass "
    "`persona=<name>` in the prompt to select; if omitted or unknown, the agent "
    "lists available specialists and asks for selection."
)


def render_meta_skill() -> str:
    """Render the /ce-ask single-slash-command catalog/dispatch surface.

    Catalog is read from manifest.json at runtime — body stays stable across
    upstream tag bumps even when persona list changes. Per-persona wrappers
    (/ce-ask-<persona>) provide tab-complete-friendly explicit invocation; this
    meta-skill provides the discoverable single-command surface for users who
    don't have a specific persona name in mind yet.
    """
    return f"""\
---
name: ce-ask
description: {json.dumps(META_SKILL_DESCRIPTION)}
argument-hint: "[persona-name [task context]]"
---

{WRAPPER_GENERATED_MARKER}

The single discoverable entry point to ce-lite specialist personas. Three modes
based on argument shape.

## Usage

- `/ce-ask` — print the persona catalog (name + specialty for each).
- `/ce-ask <persona>` — show one persona's full role definition.
- `/ce-ask <persona> <task>` — dispatch that persona on the task.

Persona names are the canonical names from this plugin's persona manifest
(e.g. `ce-security-sentinel`, `ce-architecture-strategist`). Argument
parsing: first whitespace-delimited token is the persona name; everything
after is the task context.

{PROMPT_DISCOVERY_INSTRUCTIONS}

## Steps

1. **No args** — locate the plugin root via discovery instructions above,
   read `<plugin-root>/references/agent-prompts/manifest.json`, and print
   one line per `agents[*]` entry: `<name> — <description>`. Group loosely
   by role family (review, document, design, etc.) if helpful. Stop.

2. **Persona name only** — validate against `manifest.json`. If unknown:
   > Persona `<name>` not in the catalog. Run `/ce-ask` (no args) to see all
   > available personas.
   If known, read `<plugin-root>/references/agent-prompts/<name>.md` and
   present the persona's role definition along with manifest metadata
   (tools, model). Stop.

3. **Persona name + task** — dispatch via the `Agent` tool with
   `subagent_type: "general-purpose"`. Prepend this preamble to the persona's
   prompt body, then append the user's task context:

   ```
   [ce-persona=<persona> via=ce-ask-meta]

   You are operating as the <persona> specialist. Your role is defined in the
   prompt body above. Manifest declares this role uses
   tools=[<tools-from-manifest>] and model=<model>. Honour those constraints:
   if a task pulls you toward tools outside that set, stop and explain why
   your role requires it — don't silently broaden scope.
   ```

## Notes

- Tab-complete-friendly explicit invocation: `/ce-ask-<persona-name>` (one
  per persona, generated alongside this meta-skill).
- Parallel multi-persona dispatch: `/ce-ask-panel <a>,<b>,<c> <task>`.
- Autonomous specialist consultation: the harness invokes the
  `ce-specialist` registered agent directly via `Task` when reviewing
  warrants a specialist perspective; that path doesn't go through this skill.
"""


def render_meta_agent() -> str:
    """Render the ce-specialist meta-agent.

    Single registered agent costs ~2k tokens at session start (vs ~58.8k for
    v1's 49 individual registrations). The harness considers registered
    agents during autonomous routing more aggressively than skills — this
    layer breaks the recall ceiling measured on per-skill routing in
    Phase C dry-run. Catalog is read from manifest.json at runtime so the
    agent body stays stable across upstream tag bumps.
    """
    return f"""\
---
name: ce-specialist
description: {json.dumps(META_AGENT_DESCRIPTION)}
tools:
  - Read
  - Grep
  - Glob
  - Bash
model: inherit
---

{WRAPPER_GENERATED_MARKER}

You are a router agent for ce-lite specialist personas. Your role is to adopt
the persona named in the user's prompt and respond from that specialist's
perspective. The full persona prompts live as data files alongside this
plugin; you select one by name and embody it.

{PROMPT_DISCOVERY_INSTRUCTIONS}

## Steps

1. **Parse `persona=<name>`** from the user's prompt. If absent, scan the
   prompt for an obvious persona-name reference (one of the canonical
   `ce-*` names from the plugin manifest).
2. **Validate**: locate the plugin root via discovery instructions above
   and read `<plugin-root>/references/agent-prompts/manifest.json`.
   Confirm the persona exists in `agents[*].name`. If absent or unknown,
   list all available personas grouped by role family (review, document
   review, design, research, etc.) and ask the user to specify. Do NOT
   default to a guess — partial dispatch confuses output.
3. **Adopt the persona**: read its full prompt body from
   `<plugin-root>/references/agent-prompts/<name>.md`. That prompt body
   completely defines your role for this task — follow its instructions,
   reporting protocols, and operational guidelines exactly.
4. **Honour the persona's manifest constraints**: the manifest entry
   declares `tools` and `model` for this role. Treat those as
   self-imposed constraints — if a task pulls you toward tools outside
   that set, stop and explain why your role requires it.
5. **Respond to the user's task** from the adopted persona's perspective.
   Begin your response with the trace marker
   `[ce-persona=<name> via=ce-specialist-agent]` so analytics can grep
   transcripts for routing patterns when something looks off.

## Why this agent exists

ce-lite v1 stripped the ~49 individual specialist agent registrations from
upstream compound-engineering to recover ~58.8k tokens of idle context. That
saved a lot but lost the harness's autonomous-routing capability — Claude
no longer reaches for a security-sentinel on its own when reviewing
auth code, because there's nothing for it to reach for.

This meta-agent restores that capability at ~2k tokens (one registration
instead of 49). The harness sees a single specialist-router and routes
review/analysis tasks through it; the agent then internally adopts the
right persona based on the task and the persona catalog.

For explicit user invocation, prefer:
- `/ce-ask-<persona>` — tab-complete-friendly per-persona slash commands.
- `/ce-ask <persona> <task>` — single discoverable slash command.
- `/ce-ask-panel <a>,<b>,<c> <task>` — parallel multi-persona dispatch.

This agent is the **autonomous-reach** layer; the slash commands are the
**explicit-reach** layer. Both compose; both are useful for different
moments.
"""


def write_meta_skill(dist: Path) -> Path:
    skill_dir = dist / "skills" / "ce-ask"
    skill_dir.mkdir(parents=True, exist_ok=True)
    skill_path = skill_dir / "SKILL.md"
    skill_path.write_text(render_meta_skill(), encoding="utf-8")
    return skill_path


def write_meta_agent(dist: Path) -> Path:
    agents_dir = dist / "agents"
    agents_dir.mkdir(parents=True, exist_ok=True)
    agent_path = agents_dir / "ce-specialist.agent.md"
    agent_path.write_text(render_meta_agent(), encoding="utf-8")
    return agent_path


def main(dist_arg: str, converter_arg: str | None = None) -> int:
    dist = Path(dist_arg).resolve()
    if not dist.is_dir():
        print(f"generate_wrappers.py: dist not found: {dist}", file=sys.stderr)
        return 2

    converter_dir = (
        Path(converter_arg).resolve() if converter_arg else Path(__file__).resolve().parent
    )

    personas = load_manifest(dist)
    overrides = load_overrides(converter_dir)

    used_overrides = 0
    written: list[Path] = []
    for persona in personas:
        description = overrides.get(persona.name, passA_description(persona.description))
        if persona.name in overrides:
            used_overrides += 1
        written.append(write_wrapper(dist, persona, description))

    panel_path = write_panel(dist, personas)
    meta_skill_path = write_meta_skill(dist)
    meta_agent_path = write_meta_agent(dist)

    print(
        f"generate_wrappers.py: wrote {len(written)} wrappers "
        f"({used_overrides} from overrides, {len(written) - used_overrides} via Pass A) "
        f"+ ce-ask-panel meta-skill + ce-ask discovery skill + ce-specialist meta-agent",
        file=sys.stderr,
    )
    return 0


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description=__doc__.split("\n\n", 1)[0])
    parser.add_argument("dist", help="path to converted plugin dist/ directory")
    parser.add_argument(
        "--converter-dir",
        default=None,
        help="path to converter/ source dir (default: this script's parent)",
    )
    args = parser.parse_args()
    sys.exit(main(args.dist, args.converter_dir))
